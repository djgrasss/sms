      List getUser() {
        String column = ((UserTypeComboItem) criteriaCombo5.getSelectedItem()).getColumn();
        String keyText = pkText.getText().trim();
        Query q = BankingPUEntityManager.createQuery("SELECT m  FROM Members m where m." + column + " = '" + keyText + "'");
        List l = q.getResultList();
        return l;
    }

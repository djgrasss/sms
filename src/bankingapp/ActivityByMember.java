    private void pkTextKeyReleased1(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pkTextKeyReleased1
        List l = getUser();
        Members m = null;
        if (l.size() != 0) {
            jButton1.setEnabled(true);
            m = (Members) l.get(0);
            detailsLabel.setText("<html>Username :" + m.getUsername() + " <br /> Membership No. :" + m.getNumber() + " </html>");
            Query q = BankingPUEntityManager.createNativeQuery("SELECT distinct scheme FROM accounts inner join schemes on accounts.scheme_id = schemes.id  where member_id ="+m.getId()+" and closed = 0");
            List ll = q.getResultList();
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(ll.toArray());
            typesCombo.setModel(dcm);
            System.out.println(ll.size());
        } else {
            DefaultComboBoxModel dcm = new DefaultComboBoxModel();
            typesCombo.setModel(dcm);
            jButton1.setEnabled(false);
            
        }
    }//GEN-LAST:event_pkTextKeyReleased1
    public Members getMember(){
        List l = getUser();
        Members m = null;
        if (l.size() != 0) {
             m = (Members) l.get(0);
        }
        return m;
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Vector selected =  (Vector) typesCombo.getItemAt(typesCombo.getSelectedIndex());
        String sel = (String) selected.get(0);
        System.out.println("------------"+sel);
        if(sel.equals("FD")){
            if(jRadioButton1.isSelected()){
                bw.doAction(BankingView.ACTION_FDDEPOSIT);
            }
            else{
                bw.doAction(BankingView.ACTION_FDWITHDRAW);
            }
        }
        if(sel.equals("SB")){
            if(jRadioButton1.isSelected()){
                bw.doAction(BankingView.ACTION_SBDEPOSIT);
            }
            else{
                bw.doAction(BankingView.ACTION_SBWITHDRAW);
            }
        }
        if(sel.equals("mds")){
            bw.doAction(BankingView.ACTION_MDS);
        }
        if(sel.equals("Short Term Loan")){
            bw.doAction(BankingView.ACTION_LOAN);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
    

    void populateAccountNumber(){
        Schemes scheme = (Schemes) jComboBox1.getSelectedItem();
        List l = getUser();
        Members m = null;
        if (l.size() != 0) {
            jButton1.setEnabled(true);
            m = (Members) l.get(0);
            detailsLabel.setText("<html>Username :" + m.getUsername() + " <br /> Membership No. :" + m.getNumber() + " </html>");
           
        }
        if(m==null||scheme==null){
            return;
        }
        String sql = "SELECT a FROM Accounts a WHERE a.schemeId ="+scheme.getId()+" and a.memberId = "+m.getId();
        System.out.println(sql);
        Query q = BankingPUEntityManager.createQuery(sql);
        List lst = q.getResultList();
        accountNumberCombo.setModel(new DefaultComboBoxModel(lst.toArray()));
    }

    
    

    private void populateTable() {
        Schemes scheme = (Schemes) jComboBox1.getSelectedItem();
        Accounts acc = (Accounts) accountNumberCombo.getSelectedItem();
        String[] columns = {"Reciepts", "Payments", "Date"};
        String sql = "SELECT transactions.deposit,transactions.withdrawal,transactions.`timestamp` FROM `banking`.`transactions` inner join accounts on accounts.id = transactions.account_id inner join schemes on schemes.id = accounts.scheme_id where accounts.scheme_id = "+scheme.getId()+" and accounts.id = "+acc.getId();
        sql+= " order by transactions.`timestamp` desc ";
        System.out.println(sql);
        Query q = BankingPUEntityManager.createNativeQuery(sql);
        List l = q.getResultList();
        String[][] data = new String[l.size()][3];
        int i = 0;
        for (Iterator it = l.iterator(); it.hasNext();) {
            Object object = it.next();
            Vector v = (Vector) object;
            
            data[i][0] = ((Double) v.get(0)) == null ? "" : ((Double) v.get(0)).toString();
            data[i][1] = ((Double) v.get(1)) == null ? "" : ((Double) v.get(1)).toString();

            data[i][2] = ((java.sql.Timestamp) v.get(2)).toString();
            
            i++;
        }
        DefaultTableModel dtm = new DefaultTableModel(data, columns);
        jTable1.setModel(dtm);
    }
